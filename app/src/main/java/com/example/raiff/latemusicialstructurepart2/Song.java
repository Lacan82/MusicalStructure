package com.example.raiff.latemusicialstructurepart2;

public class Albums {

    private String mAlbums;
    private int mAlbumsImage;


    public Albums(String Albums, int Image) {
        mAlbums = Albums;
        mAlbumsImage = Image;
    }

    public String getAlbums() {
        return mAlbums;
    }

    public int getAlbumsImage() {
        return mAlbumsImage;
    }
}
